def resolve_city(name: str) -> tuple[int, str] | None:
    """Return (geo_id, canonical_name) or None if not found."""
    key = name.strip().lower()
    geo_id = _CITY_TO_GEO_ID.get(key)
    if geo_id is None:
        return None
    canonical = name.strip().title()
    return geo_id, canonical


def popular_cities_hint() -> str:
    return ", ".join(_POPULAR)
